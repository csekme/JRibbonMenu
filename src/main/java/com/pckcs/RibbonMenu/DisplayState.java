package com.pckcs.RibbonMenu;

/** The Enum DisplayState. */
public enum DisplayState {
  /** leave icon same size */
  NORMAL,
  /** scale icon to 16x16 */
  SLIM,
  /** scale icon to 24x24 */
  MEDIUM,
  /** scale icon to 32x32 */
  LARGE,
  /** quick access buttons 22x22 */
  QUICK,
  /** indicate a separator. */
  SEPARATOR
}
