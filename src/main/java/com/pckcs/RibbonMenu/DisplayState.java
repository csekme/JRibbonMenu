package com.pckcs.RibbonMenu;

/**
 * The Enum DisplayState.
 */
public enum DisplayState {
        /** leave icon same size */   NORMAL,
        /** scale icon to 16x16 */    SMALL,
        /** scale icon to 22x22 */    MEDIUM,
        /** scale icon to 32x32 */    LARGE,
        /** indicate a separator. */  SEPARATOR
}
